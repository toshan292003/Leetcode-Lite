int solve(vector<int> nums){
    if(nums.size()>10){
        return 0;
    }
    int prev = nums[0],max = 0;
    for(int i=1;i<nums.size();i++){
        int a = abs(nums[i]-prev);
        prev = nums[i];
        if(max<a){
            max = a;
        }
    }


    return max;
}


